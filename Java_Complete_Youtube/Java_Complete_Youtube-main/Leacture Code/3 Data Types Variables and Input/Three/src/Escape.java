public class Escape {
    public static void main(String[] args) {

        System.out.println("Hello\tKGCoding..."); //insert a tab in the text at the point
        System.out.println("Hello\bKGCoding..."); //insert a backspace at the point
        System.out.println("Hello\nKGCoding..."); //insert a newline
        System.out.println("Hello\'KGCoding..."); //insert a single quote in string
        System.out.println("Hello\"KGCoding..."); //insert a double quote in string
        System.out.println("Hello\\KGCoding..."); //insert a backslash in a string

//        Hello	KGCoding...
//        HellKGCoding...
//        Hello
//        KGCoding...
//        Hello'KGCoding...
//        Hello"KGCoding...
//        Hello\KGCoding...

    }
}
